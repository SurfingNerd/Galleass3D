        public static Task<TransactionReceipt> DeployContractAndWaitForReceiptAsync(Nethereum.Web3.Web3 web3, TimberDeployment timberDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            return web3.Eth.GetContractDeploymentHandler<TimberDeployment>().SendRequestAndWaitForReceiptAsync(timberDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, TimberDeployment timberDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<TimberDeployment>().SendRequestAsync(timberDeployment);
        }

        public static async Task<TimberService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, TimberDeployment timberDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, timberDeployment, cancellationTokenSource);
            return new TimberService(web3, receipt.ContractAddress);
        }

        protected Nethereum.Web3.Web3 Web3{ get; }

