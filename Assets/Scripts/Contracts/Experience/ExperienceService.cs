            return web3.Eth.GetContractDeploymentHandler<ExperienceDeployment>().SendRequestAndWaitForReceiptAsync(experienceDeployment, cancellationTokenSource);
        }

        public static Task<string> DeployContractAsync(Nethereum.Web3.Web3 web3, ExperienceDeployment experienceDeployment)
        {
            return web3.Eth.GetContractDeploymentHandler<ExperienceDeployment>().SendRequestAsync(experienceDeployment);
        }

        public static async Task<ExperienceService> DeployContractAndGetServiceAsync(Nethereum.Web3.Web3 web3, ExperienceDeployment experienceDeployment, CancellationTokenSource cancellationTokenSource = null)
        {
            var receipt = await DeployContractAndWaitForReceiptAsync(web3, experienceDeployment, cancellationTokenSource);
            return new ExperienceService(web3, receipt.ContractAddress);
        }

